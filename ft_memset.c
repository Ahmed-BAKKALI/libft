#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*d;
	size_t			i;

	d = (unsigned char *)b;
	i = 0;
	while (len--)
	{
		d[i] = (unsigned char)c;
		i++;
	}
	return (d);
}
